#include <minitalk.h>

int		main(void)
{
	ft_putnbr(getpid());
	write(1, "\n", 1);
	


	
	return (0);
}

void	ft_putnbr(int n)
{
	char	c;

	if (n >= 10)
		ft_putnbr(n / 10);
	c = n % 10 + '0';
	write(1, &c, 1);
}
